package framework.entity.vo;

public class CommodityVo {
	
	//商品id
	private Long commodity_id;
	
	//商品名称
	private  String commodity_name;
	
	//商品图片
	private String commodity_url;
	
	//商品数量
	private Integer commodity_num;
	
	//商品价格
	private Double commodity_price;

	public Long getCommodity_id() {
		return commodity_id;
	}

	public void setCommodity_id(Long commodity_id) {
		this.commodity_id = commodity_id;
	}

	public String getCommodity_name() {
		return commodity_name;
	}

	public void setCommodity_name(String commodity_name) {
		this.commodity_name = commodity_name;
	}

	public String getCommodity_url() {
		return commodity_url;
	}

	public void setCommodity_url(String commodity_url) {
		this.commodity_url = commodity_url;
	}

	public Integer getCommodity_num() {
		return commodity_num;
	}

	public void setCommodity_num(Integer commodity_num) {
		this.commodity_num = commodity_num;
	}

	public Double getCommodity_price() {
		return commodity_price;
	}

	public void setCommodity_price(Double commodity_price) {
		this.commodity_price = commodity_price;
	}
	
	
	

}
